/* World-map configuration — COMPANION CAMPAIGNS.

   Progression belongs to the active companion's CAMPAIGN, not the hero's
   lifetime totals. Every campaign walks the same three SHARED WORLDS
   (Shadow Ninja Village → Legend of the Samurai → Speed Realm) and ends in
   that companion's exclusive FINALE WORLD — 4 worlds × 36 quests = 144 steps.
   Campaign progress = the bond's quests_done (starts at 0 for every new
   partner); completing the finale makes the companion LEGENDARY.

   Each world spans 36 quests and paints 36 nodes onto its map: every
   approved quest moves the hero one step along the path. Node positions are
   generated by interpolating along hand-placed percentage ANCHORS that trace
   the painted path (x = % from left, y = % from top) — tweak the anchors to
   re-shape a world's route, no other changes needed.

   Along the way, seven named LANDMARKS mark memorable places, and node 36 is
   the world's boss-style trial. */

import { ThemeId, CompanionBond } from "./game";
import { worldMapArt } from "./assets";

export type NodeKind = "quest" | "landmark" | "final";

export interface MapNode {
  id: string;
  name: string;
  x: number; // % from left of the map image
  y: number; // % from top of the map image
  requires: number; // total quests completed needed to clear this node
  kind: NodeKind;
}

export interface WorldMapDef {
  id: string; // matches the public/worlds/<id>/ folder
  name: string;
  /** Set for the three shared worlds; finale worlds have no theme/palette. */
  theme?: ThemeId;
  map: string; // path under /public
  accent: string; // themed glow colour for the path + nodes
  /** Hand-placed waypoints tracing the painted path — adjust these freely. */
  anchors: { x: number; y: number }[];
  /** The boss-style milestone that closes the world (node 36). */
  finale: { name: string; icon: string; blurb: string };
  /** What completing the world unlocks. */
  reward: { companion: string; blurb: string };
  levels: MapNode[];
}

