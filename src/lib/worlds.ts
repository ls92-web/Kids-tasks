/* Evenly space `count` points along the anchor polyline (by arc length),
   so nodes follow the painted path no matter how anchors are spaced. */
function tracePath(anchors: { x: number; y: number }[], count: number) {
  const segs: number[] = [];
  let total = 0;
  for (let i = 1; i < anchors.length; i++) {
    const d = Math.hypot(anchors[i].x - anchors[i - 1].x, anchors[i].y - anchors[i - 1].y);
    segs.push(d);
    total += d;
  }
  const pts: { x: number; y: number }[] = [];
  for (let n = 0; n < count; n++) {
    const target = (n / (count - 1)) * total;
    let acc = 0;
    let i = 0;
    while (i < segs.length - 1 && acc + segs[i] < target) {
      acc += segs[i];
      i += 1;
    }
    const t = segs[i] === 0 ? 0 : (target - acc) / segs[i];
    pts.push({
      x: +(anchors[i].x + (anchors[i + 1].x - anchors[i].x) * t).toFixed(2),
      y: +(anchors[i].y + (anchors[i + 1].y - anchors[i].y) * t).toFixed(2),
    });
  }
  return pts;
}

/* 36 nodes along a path: quiet steps, 7 named landmarks, the trial at 36.
   worldIdx positions the requires values within the campaign (0-3). */
function buildLevels(
  idPrefix: string,
  anchors: { x: number; y: number }[],
  stops: string[],
  finaleName: string,
  worldIdx: number
): MapNode[] {
  const pts = tracePath(anchors, CHAPTER_SPAN);
  return pts.map((p, i) => {
    const step = i + 1;
    const landmarkIdx = LANDMARK_STEPS.indexOf(step);
    const kind: NodeKind =
      step === CHAPTER_SPAN ? "final" : landmarkIdx >= 0 ? "landmark" : "quest";
    return {
      id: `${idPrefix}-${step}`,
      name:
        kind === "final" ? finaleName : kind === "landmark" ? stops[landmarkIdx] : `Step ${step}`,
      x: p.x,
      y: p.y,
      requires: worldIdx * CHAPTER_SPAN + step,
      kind,
    };
  });
}

function buildWorld(spec: WorldSpec, chapterIdx: number): WorldMapDef {
  return {
    ...spec,
    levels: buildLevels(spec.theme, spec.anchors, spec.stops, spec.finale.name, chapterIdx),
  };
}

