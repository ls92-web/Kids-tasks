/* A finale world with delivered map art becomes a full, renderable world:
   36 nodes at campaign steps 109..144, ending at the Legend Ceremony gate. */
export function finaleWorldMap(species: string): WorldMapDef | null {
  const fw = FINALE_WORLDS[species];
  if (!fw?.map || !fw.anchors || !fw.stops) return null;
  return {
    id: fw.id,
    name: fw.name,
    map: fw.map,
    accent: fw.accent,
    anchors: fw.anchors,
    finale: fw.finale,
    reward: { companion: species, blurb: `${fw.name} complete — Legendary!` },
    levels: buildLevels(
      `${species}-finale`,
      fw.anchors,
      fw.stops,
      fw.finale.name,
      SHARED_WORLDS.length
    ),
  };
}

export type NodeState = "completed" | "current" | "locked";

/** State of every node given how many quests the child has completed. */
export function nodeStates(levels: MapNode[], tasksCompleted: number): NodeState[] {
  let currentAssigned = false;
  return levels.map((lvl) => {
    if (tasksCompleted >= lvl.requires) return "completed";
    if (!currentAssigned) {
      currentAssigned = true;
      return "current";
    }
    return "locked";
  });
}

/** Progress summary for captions: cleared count + the node you're on. */
export function worldProgress(world: WorldMapDef, tasksCompleted: number) {
  const states = nodeStates(world.levels, tasksCompleted);
  const completed = states.filter((s) => s === "completed").length;
  const currentIdx = states.indexOf("current");
  return {
    completed,
    total: world.levels.length,
    current: currentIdx >= 0 ? world.levels[currentIdx] : null,
    states,
  };
}

/* ---------- Campaign progression ----------
   All functions below take the CAMPAIGN STEP: the active bond's steps_done —
   difficulty-weighted (easy/medium 1, hard 2, epic 3) so real effort moves
   the map faster. Every new companion campaign starts at step 0 and runs to
   CAMPAIGN_TOTAL. Node `requires` values (1..108) are campaign steps, so the
   maps read the step directly. The finale world occupies steps 109..144. */

/** The active campaign's progress — weighted steps earned during this bond
    (older cached rows without steps_done fall back to the quest count). */
export function campaignStep(
  bond: Pick<CompanionBond, "quests_done" | "steps_done"> | null
): number {
  return Math.min(bond?.steps_done ?? bond?.quests_done ?? 0, CAMPAIGN_TOTAL);
}

/** Which of the 4 campaign worlds the hero is in right now (0-3). */
export function campaignWorldIndex(step: number): number {
  return Math.min(Math.floor(step / CHAPTER_SPAN), WORLDS_PER_CAMPAIGN - 1);
}

/** True once the campaign has reached its companion-exclusive finale world. */
export function inFinaleWorld(step: number): boolean {
  return step >= CHAPTER_SPAN * SHARED_WORLDS.length && step < CAMPAIGN_TOTAL;
}

/** Campaign completed = the finale world's last trial is cleared. */
export function campaignCompleted(step: number): boolean {
  return step >= CAMPAIGN_TOTAL;
}

/** A shared world is complete once its final trial is cleared this campaign. */
export function worldCompleted(theme: ThemeId, step: number): boolean {
  const levels = WORLD_MAPS[theme].levels;
  return step >= levels[levels.length - 1].requires;
}

/** How many SHARED worlds this campaign has fully cleared (0-3). */
export function worldsCompleted(step: number): number {
  let n = 0;
  for (const t of SHARED_WORLDS) {
    if (worldCompleted(t, step)) n += 1;
    else break;
  }
  return n;
}

/** A shared world opens once every world before it is complete (this campaign). */
export function worldUnlocked(theme: ThemeId, step: number): boolean {
  const idx = SHARED_WORLDS.indexOf(theme);
  return idx <= worldsCompleted(step);
}

/** The shared world whose MAP should be shown right now (the finale world has
    no painted map yet, so its segment keeps showing the last shared map). */
export function currentChapter(step: number): ThemeId {
  return SHARED_WORLDS[Math.min(worldsCompleted(step), SHARED_WORLDS.length - 1)];
}

/** The next shared world after this one, if any. */
export function nextChapter(theme: ThemeId): ThemeId | null {
  const idx = SHARED_WORLDS.indexOf(theme);
  return idx >= 0 && idx < SHARED_WORLDS.length - 1 ? SHARED_WORLDS[idx + 1] : null;
}

/** The world map the active campaign should RENDER right now:
    the child's chosen shared world during worlds 1-3, then the companion's
    exclusive finale world once its segment begins (falling back to the last
    shared map for companions whose finale art hasn't been delivered yet). */
export function campaignWorld(species: string, theme: ThemeId, step: number): WorldMapDef {
  if (step >= CHAPTER_SPAN * SHARED_WORLDS.length) {
    return finaleWorldMap(species) ?? WORLD_MAPS[currentChapter(step)];
  }
  return WORLD_MAPS[theme];
}

/* ---------- Lifetime progression (species unlocks only) ----------
   Companion unlock rules stay keyed to the hero's LIFETIME totals — once a
   world has been cleared in any campaign it stays cleared for unlock
   purposes. Mirrors the thresholds in the bond_companion SQL function. */

export function lifetimeWorldsCleared(lifetimeSteps: number): number {
  let n = 0;
  for (let i = 0; i < SHARED_WORLDS.length; i++) {
    if (lifetimeSteps >= (i + 1) * CHAPTER_SPAN) n += 1;
    else break;
  }
  return n;
}

/** Lifetime weighted steps across every bond — the input for
    lifetimeWorldsCleared (mirrors the sum in bond_companion SQL). */
export function lifetimeSteps(bonds: Pick<CompanionBond, "quests_done" | "steps_done">[]): number {
  return bonds.reduce((sum, b) => sum + (b.steps_done ?? b.quests_done ?? 0), 0);
}
