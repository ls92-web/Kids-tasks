/* World-map configuration — worlds are CHAPTERS of one adventure.

   Each chapter spans 36 quests and paints 36 nodes onto its map: every
   completed quest moves the hero one step along the path. Node positions are
   generated by interpolating along hand-placed percentage ANCHORS that trace
   the painted path (x = % from left, y = % from top) — tweak the anchors to
   re-shape a world's route, no other changes needed.

   Along the way, seven named LANDMARKS mark memorable places, and node 36 is
   the FINAL CHAPTER CHALLENGE — a boss-style trial. Clearing it completes the
   chapter: a companion awakens, and the next world unlocks. */

import { ThemeId } from "./game";

export type NodeKind = "quest" | "landmark" | "final";

export interface MapNode {
  id: string;
  name: string;
  x: number; // % from left of the map image
  y: number; // % from top of the map image
  requires: number; // total quests completed needed to clear this node
  kind: NodeKind;
}

export interface WorldMapDef {
  id: string; // matches the public/worlds/<id>/ folder
  name: string;
  theme: ThemeId;
  map: string; // path under /public
  accent: string; // themed glow colour for the path + nodes
  /** Hand-placed waypoints tracing the painted path — adjust these freely. */
  anchors: { x: number; y: number }[];
  /** The boss-style milestone that closes the chapter (node 36). */
  finale: { name: string; icon: string; blurb: string };
  /** What completing the chapter unlocks. */
  reward: { companion: string; blurb: string };
  levels: MapNode[];
}

/* Every chapter is 36 quests; landmarks sit at these steps within it. */
export const CHAPTER_SPAN = 36;
const LANDMARK_STEPS = [1, 4, 8, 13, 18, 24, 30];

/* The adventure's chapter order — complete one world to unlock the next. */
export const WORLD_ORDER: ThemeId[] = ["ninja", "samurai", "speed"];

