/* Evenly space `count` points along the anchor polyline (by arc length),
   so nodes follow the painted path no matter how anchors are spaced. */
function tracePath(anchors: { x: number; y: number }[], count: number) {
  const segs: number[] = [];
  let total = 0;
  for (let i = 1; i < anchors.length; i++) {
    const d = Math.hypot(anchors[i].x - anchors[i - 1].x, anchors[i].y - anchors[i - 1].y);
    segs.push(d);
    total += d;
  }
  const pts: { x: number; y: number }[] = [];
  for (let n = 0; n < count; n++) {
    const target = (n / (count - 1)) * total;
    let acc = 0;
    let i = 0;
    while (i < segs.length - 1 && acc + segs[i] < target) {
      acc += segs[i];
      i += 1;
    }
    const t = segs[i] === 0 ? 0 : (target - acc) / segs[i];
    pts.push({
      x: +(anchors[i].x + (anchors[i + 1].x - anchors[i].x) * t).toFixed(2),
      y: +(anchors[i].y + (anchors[i + 1].y - anchors[i].y) * t).toFixed(2),
    });
  }
  return pts;
}

function buildWorld(spec: WorldSpec, chapterIdx: number): WorldMapDef {
  const pts = tracePath(spec.anchors, CHAPTER_SPAN);
  const levels: MapNode[] = pts.map((p, i) => {
    const step = i + 1;
    const landmarkIdx = LANDMARK_STEPS.indexOf(step);
    const kind: NodeKind =
      step === CHAPTER_SPAN ? "final" : landmarkIdx >= 0 ? "landmark" : "quest";
    return {
      id: `${spec.theme}-${step}`,
      name:
        kind === "final"
          ? spec.finale.name
          : kind === "landmark"
            ? spec.stops[landmarkIdx]
            : `Step ${step}`,
      x: p.x,
      y: p.y,
      requires: chapterIdx * CHAPTER_SPAN + step,
      kind,
    };
  });
  return { ...spec, levels };
}

export const WORLD_MAPS: Record<ThemeId, WorldMapDef> = Object.fromEntries(
  SPECS.map((s, i) => [s.theme, buildWorld(s, i)])
) as Record<ThemeId, WorldMapDef>;

export type NodeState = "completed" | "current" | "locked";

/** State of every node given how many quests the child has completed. */
export function nodeStates(levels: MapNode[], tasksCompleted: number): NodeState[] {
  let currentAssigned = false;
  return levels.map((lvl) => {
    if (tasksCompleted >= lvl.requires) return "completed";
    if (!currentAssigned) {
      currentAssigned = true;
      return "current";
    }
    return "locked";
  });
}

/** Progress summary for captions: cleared count + the node you're on. */
export function worldProgress(world: WorldMapDef, tasksCompleted: number) {
  const states = nodeStates(world.levels, tasksCompleted);
  const completed = states.filter((s) => s === "completed").length;
  const currentIdx = states.indexOf("current");
  return {
    completed,
    total: world.levels.length,
    current: currentIdx >= 0 ? world.levels[currentIdx] : null,
    states,
  };
}

/* ---------- Chapters ----------
   Worlds are chapters of one adventure, not skins. A chapter is complete when
   its final challenge is cleared; only then does the next chapter open. */

export function worldCompleted(theme: ThemeId, tasksCompleted: number): boolean {
  const levels = WORLD_MAPS[theme].levels;
  return tasksCompleted >= levels[levels.length - 1].requires;
}

/** How many chapters the hero has fully cleared (0-3). */
export function worldsCompleted(tasksCompleted: number): number {
  let n = 0;
  for (const t of WORLD_ORDER) {
    if (worldCompleted(t, tasksCompleted)) n += 1;
    else break;
  }
  return n;
}

/** A chapter opens once every chapter before it is complete. */
export function worldUnlocked(theme: ThemeId, tasksCompleted: number): boolean {
  const idx = WORLD_ORDER.indexOf(theme);
  return idx <= worldsCompleted(tasksCompleted);
}

/** The chapter the hero should be adventuring in right now. */
export function currentChapter(tasksCompleted: number): ThemeId {
  return WORLD_ORDER[Math.min(worldsCompleted(tasksCompleted), WORLD_ORDER.length - 1)];
}

/** The next chapter after this one, if any. */
export function nextChapter(theme: ThemeId): ThemeId | null {
  const idx = WORLD_ORDER.indexOf(theme);
  return idx >= 0 && idx < WORLD_ORDER.length - 1 ? WORLD_ORDER[idx + 1] : null;
}
